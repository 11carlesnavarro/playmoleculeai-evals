"""Populate a BixBench eval set from a Hugging Face dataset.

Re-runnable. Setup::

    uv sync --extra bixbench
    uv run python scripts/fetch_bixbench.py --variant full        # 205 questions
    uv run python scripts/fetch_bixbench.py --variant verified    # 50 questions
"""

from __future__ import annotations

import argparse
import json
import logging
import re
import shutil
import sys
import zipfile
from collections.abc import Iterable
from dataclasses import dataclass
from pathlib import Path
from typing import Any

from ruamel.yaml import YAML
from ruamel.yaml.scalarstring import LiteralScalarString

logger = logging.getLogger("fetch_bixbench")


@dataclass(frozen=True)
class Variant:
    repo_id: str
    questions_file: str
    eval_set_dir: Path

    @property
    def fixtures_dir(self) -> Path:
        return self.eval_set_dir / "fixtures"

    @property
    def cases_path(self) -> Path:
        return self.eval_set_dir / "cases.yaml"


VARIANTS: dict[str, Variant] = {
    "full": Variant(
        repo_id="futurehouse/BixBench",
        questions_file="BixBench.jsonl",
        eval_set_dir=Path("eval_sets/bixbench"),
    ),
    "verified": Variant(
        repo_id="phylobio/BixBench-Verified-50",
        questions_file="BixBench-Verified-50.jsonl",
        eval_set_dir=Path("eval_sets/bixbench-verified"),
    ),
}

VERIFIER_FOR_MODE = {
    "str_verifier": "bix_str_verifier",
    "range_verifier": "bix_range_verifier",
    "llm_verifier": "bix_llm_verifier",
}

# Minimal: the dataset's raw question, plus a one-liner that points the grader's
# extractor at <answer>...</answer>. The grader is BixBench's own; everything
# beyond this lived in their aviary harness, not in the eval.
ANSWER_INSTRUCTION = (
    "Wrap your final answer in <answer>...</answer> tags."
)


def _cases_header(variant: Variant) -> str:
    return (
        f"# Generated by scripts/fetch_bixbench.py from {variant.repo_id}.\n"
        "# Edit the script, not this file.\n"
    )


# ---- HF data plumbing ---------------------------------------------------

def _hf_download(variant: Variant, file_name: str) -> Path:
    try:
        from huggingface_hub import hf_hub_download  # pyright: ignore[reportMissingImports]
    except ImportError as exc:
        sys.exit(
            f"missing 'huggingface_hub' library: {exc}. "
            f"Install with: uv sync --extra bixbench"
        )
    return Path(
        hf_hub_download(
            repo_id=variant.repo_id, filename=file_name, repo_type="dataset"
        )
    )


def _load_questions(variant: Variant) -> list[dict[str, Any]]:
    logger.info("loading %s/%s from Hugging Face", variant.repo_id, variant.questions_file)
    path = _hf_download(variant, variant.questions_file)
    return [json.loads(line) for line in path.read_text(encoding="utf-8").splitlines() if line.strip()]


_DATA_PREFIX_RE = re.compile(r"^CapsuleData-[^/]+/")


def _is_skippable(name: str) -> bool:
    return (
        name.startswith("__MACOSX/")
        or name.endswith("/.DS_Store")
        or "CapsuleNotebook-" in name
    )


def _extract_capsule_data(src_zip: Path, dest_dir: Path) -> list[Path]:
    """Extract ``CapsuleData-*`` files from ``src_zip`` flat into ``dest_dir``.

    The original BixBench capsule zip ships both ``CapsuleData-<uuid>/`` (the
    raw input data) and ``CapsuleNotebook-<uuid>/`` (the executed notebook
    that contains the worked-out answer). Uploading the notebook would be a
    ground-truth leak, so we keep only data members and strip the
    ``CapsuleData-<uuid>/`` wrapper so the agent sees plain file names.
    """
    dest_dir.mkdir(parents=True, exist_ok=True)
    extracted: list[Path] = []
    with zipfile.ZipFile(src_zip) as src:
        for member in src.infolist():
            name = member.filename
            if member.is_dir() or _is_skippable(name):
                continue
            stripped = _DATA_PREFIX_RE.sub("", name)
            if stripped == name:  # not under CapsuleData-*/
                continue
            target = dest_dir / stripped
            target.parent.mkdir(parents=True, exist_ok=True)
            with src.open(member) as fh, target.open("wb") as out:
                shutil.copyfileobj(fh, out)
            extracted.append(target)
    return extracted


def _ensure_capsule_files(
    variant: Variant, capsule_uuid: str, file_name: str, *, download: bool
) -> list[Path]:
    """Return data-only file paths for ``capsule_uuid``.

    Caches under ``fixtures/<uuid>/``. Nested archives inside the capsule
    (BUSCO outputs, lineage DBs, etc.) are part of BixBench's data contract
    and are uploaded as-is; the agent's sandbox has ``unzip`` for any that
    need extracting.
    """
    capsule_dir = variant.fixtures_dir / capsule_uuid
    if capsule_dir.is_dir():
        return [p for p in capsule_dir.rglob("*") if p.is_file()]
    if not download:
        logger.warning(
            "capsule %s not extracted and --no-download set; case will list 0 files",
            capsule_uuid,
        )
        return []
    logger.info("downloading capsule %s (%s)", capsule_uuid, file_name)
    src_zip = _hf_download(variant, file_name)
    files = _extract_capsule_data(src_zip, capsule_dir)
    logger.info("extracted %d files into %s", len(files), capsule_dir)
    return files


# ---- case construction -------------------------------------------------

def _slug_id(question_id: str) -> str:
    """Turn ``bix-1-q1`` style ids into safe lower-kebab-case case ids."""
    text = re.sub(r"[^a-zA-Z0-9]+", "-", question_id.strip().lower())
    return text.strip("-") or "bix-unknown"


def _categories(row: dict[str, Any]) -> list[str]:
    raw = row.get("categories") or ""
    return [
        re.sub(r"[^a-z0-9]+", "-", token.strip().lower()).strip("-")
        for token in str(raw).split(",")
        if token.strip()
    ]


def _build_prompt(row: dict[str, Any]) -> str:
    question = str(row.get("question") or "").strip()
    return f"{question}\n\n{ANSWER_INSTRUCTION}"


def _build_case(row: dict[str, Any], capsule_files: list[str]) -> dict[str, Any]:
    eval_mode = str(row["eval_mode"])
    case_id = _slug_id(str(row.get("question_id") or row.get("short_id") or row.get("id")))
    return {
        "id": case_id,
        "difficulty": "hard",
        "prompt": LiteralScalarString(_build_prompt(row)),
        "tags": ["bixbench", *_categories(row)],
        "preload": {"project": {"files": capsule_files}},
        "assertions": [
            {
                "type": "python_check",
                "function": VERIFIER_FOR_MODE[eval_mode],
                "kwargs": {
                    "ideal": str(row["ideal"]),
                    "question": str(row.get("question") or ""),
                },
            }
        ],
        "rubric": {"enabled": False},
    }


# ---- writer ------------------------------------------------------------

def _write_cases(variant: Variant, cases: list[dict[str, Any]]) -> None:
    yaml = YAML()
    yaml.indent(mapping=2, sequence=4, offset=2)
    yaml.width = 4096
    with variant.cases_path.open("w", encoding="utf-8") as fh:
        fh.write(_cases_header(variant))
        yaml.dump({"cases": cases}, fh)


def _iter_unique(rows: Iterable[dict[str, Any]], key: str) -> Iterable[dict[str, Any]]:
    seen: set[str] = set()
    for row in rows:
        value = str(row.get(key) or "")
        if value and value not in seen:
            seen.add(value)
            yield row


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--variant",
        choices=sorted(VARIANTS),
        default="full",
        help="Which BixBench dataset to fetch.",
    )
    parser.add_argument("--limit", type=int, default=None, help="Process only the first N rows.")
    parser.add_argument(
        "--no-download",
        action="store_true",
        help="Skip capsule downloads; reuse whatever is already unzipped.",
    )
    parser.add_argument("--log-level", default="INFO")
    args = parser.parse_args()

    logging.basicConfig(
        level=args.log_level,
        format="%(asctime)s %(levelname)s %(name)s: %(message)s",
    )

    variant = VARIANTS[args.variant]
    if not variant.eval_set_dir.is_dir():
        sys.exit(f"eval set dir not found: {variant.eval_set_dir.resolve()}")

    rows = _load_questions(variant)
    if args.limit is not None:
        rows = rows[: args.limit]
    logger.info("got %d questions", len(rows))

    capsule_files: dict[str, list[str]] = {}
    for row in _iter_unique(rows, "capsule_uuid"):
        uuid = str(row["capsule_uuid"])
        file_name = str(row.get("data_folder") or f"CapsuleFolder-{uuid}.zip")
        files = _ensure_capsule_files(
            variant, uuid, file_name, download=not args.no_download
        )
        capsule_files[uuid] = sorted(
            str(p.relative_to(variant.fixtures_dir)) for p in files
        )

    cases: list[dict[str, Any]] = []
    seen_ids: set[str] = set()
    for row in rows:
        uuid = str(row.get("capsule_uuid") or "")
        case = _build_case(row, capsule_files.get(uuid, []))
        if case["id"] in seen_ids:
            logger.warning("duplicate case id %s; skipping", case["id"])
            continue
        seen_ids.add(case["id"])
        cases.append(case)

    cases.sort(key=lambda c: c["id"])
    _write_cases(variant, cases)
    logger.info("wrote %d cases to %s", len(cases), variant.cases_path)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
